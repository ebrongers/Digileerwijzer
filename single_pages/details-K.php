              <h3 class="blue scenarioheader">Klassiek</h3>
              <p class="profieltekst scenariotekst">De 'thuiseters' staan model voor de klassieke lessituatie. Een gevarieerde en voedzame 'maaltijd', keurig bereid volgens de schijf van vijf en dankzij het kookboek verloopt de bereiding probleemloos.
                <br>
                <br> Het onderwijs is docentgestuurd. De leraar, 'moeder' in dit model, heeft de touwtjes in handen, maar heeft er ook zijn handen aan vol. Hij bereidt de inhoud van de lessen goed voor en is intensief betrokken bij de invulling ervan. De leerling heeft ook zijn aandeel ('boodschappen doen, aardappels schillen, afruimen en de vaat'), maar dat zit meer in de marge van de les.
