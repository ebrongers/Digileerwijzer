                <br>Bij deze lessituatie is er ruimte voor het inzetten van ict en media. De 'thuiseters' in deze metafoor kunnen een vaatwasser, keukenmachine en magnetron gebruiken zonder dat dat afbreuk doet aan de kwaliteit van de maaltijd: dat verlicht de taken, biedt moeder meer tijd voor een goed gesprek of het leren koken door de andere gezinsleden en draagt zo bij aan gezelligheid tijdens het eten.&nbsp;</p>
