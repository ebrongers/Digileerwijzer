              <h3 class="blue scenarioheader">Actief zelfstandig</h3>
              <p class="profieltekst scenariotekst">De 'Amerikaanse barbecue' staat model voor een lessituatie afgestemd op actieve, zelfstandige leerlingen. Heel gevarieerd, maar de 'gerechten' zijn van wisselende kwaliteit.&nbsp;
                <br>
                <br> De leraar, in dit model de 'gastheer' van de barbecue, bouwt wel een aantal randvoorwaarden in ('de gasten mogen niet met lege handen op de barbecue komen'), maar biedt de leerlingen veel vrijheid. Hij stimuleert de zelfwerkzaamheid en de nadruk ligt meer op het proces dan op het resultaat. De leerlingen kennen het 'recept': ze moeten zelf actief bijdragen aan het eindresultaat. Maar de ene leerling stopt er meer tijd in dan de ander en 'bakt' er dan ook meer van.
                <br>
                <br> De samenwerking en afstemming tussen leerlingen is beperkt, maar ze leveren wel onderlinge feedback op elkaars prestaties. Zo organiseren ze hun eigen leerproces.
                <br>
                <br> Een lessituatie met actieve zelfstandige leerlingen gaat niet noodzakelijkerwijs gepaard met intensief mediagebruik. Maar er zijn veel ict-toepassingen die de leerling kunnen helpen om zelfstandig zijn eigen leerroute samen te stellen. Zij kunnen kiezen uit een royaal aanbod van media en nemen daar zelf ook het initiatief toe. Een elektronisch portfolio is hierbij een nuttig hulpmiddel.&nbsp;</p>
